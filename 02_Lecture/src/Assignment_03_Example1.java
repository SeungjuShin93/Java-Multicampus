package Lecture_01;

public class Assignment_03_Example1 {
	public static void main(String args[]) {
		 int num = 17;
		 num += 1; // num = num + 1; 과 동일
		 System.out.println(num);
		 num -= 2; // num = num - 2; 과 동일
		 System.out.println(num);
		 num *= 3; // num = num * 3; 과 동일
		 System.out.println(num);
		 num /= 4; // num = num / 4; 과 동일
		 System.out.println(num);
		 num %= 5; // num = num % 5; 과 동일
		 System.out.println(num);
		 }
}
